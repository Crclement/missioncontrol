interface PixelCreatureProps {
  workType: WorkType
  size?: number
  inverted?: boolean
}

export function PixelCreature({ workType, size = 3, inverted = false }: PixelCreatureProps) {
  const sprite = SPRITES[workType]
  const fillColor = inverted ? "#fff" : "#000"

  return (
    <div
      style={{
        display: "grid",
        gridTemplateColumns: `repeat(12, ${size}px)`,
        imageRendering: "pixelated",
        flexShrink: 0,
      }}
    >
      {sprite.flatMap((row, y) =>
        row.map((cell, x) => (
          <div
            key={`${y}-${x}`}
            style={{
              width: size,
              height: size,
              backgroundColor: cell === 1 ? fillColor : "transparent",
            }}
          />
        )),
      )}
    </div>
  )
}
