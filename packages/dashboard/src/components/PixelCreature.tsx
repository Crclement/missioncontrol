interface PixelCreatureProps {
  workType: WorkType
  size?: number
  inverted?: boolean
}

export function PixelCreature({ workType, size = 3, inverted = false }: PixelCreatureProps) {
  const [frame, setFrame] = useState(0)
  const frames = SPRITES[workType]

  useEffect(() => {
    // Idle animals animate slower
    const speed = workType === "idle" ? 1500 : workType === "running" ? 300 : 700
    const interval = setInterval(() => {
      setFrame((f) => (f + 1) % frames.length)
    }, speed)
    return () => clearInterval(interval)
  }, [workType, frames.length])

  const sprite = frames[frame]
  const fillColor = inverted ? "#fff" : "#000"

  return (
    <div
      style={{
        display: "grid",
        gridTemplateColumns: `repeat(12, ${size}px)`,
        imageRendering: "pixelated",
        flexShrink: 0,
      }}
    >
      {sprite.flatMap((row, y) =>
        row.map((cell, x) => (
          <div
            key={`${y}-${x}`}
            style={{
              width: size,
              height: size,
              backgroundColor: cell === 1 ? fillColor : "transparent",
            }}
          />
        )),
      )}
    </div>
  )
}
